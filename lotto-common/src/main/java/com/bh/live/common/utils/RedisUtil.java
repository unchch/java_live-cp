    public boolean setByFastJson(final String key, final Object value, final long timeout, TimeUnit timeUnit) {
        boolean result = this.redisTemplate.execute((RedisConnection connection) -> {
            RedisSerializer serializer = RedisUtil.this.redisTemplate.getStringSerializer();
            try {
                connection.set(serializer.serialize(key), serializer.serialize(JSON.toJSONString(value)));
                RedisUtil.this.redisTemplate.expire(key, timeout, timeUnit);
            } catch (Exception var4) {
                log.error("Redis 异常", var4);
                throw new RuntimeException(var4.getMessage());
            }
            return true;
        });
        return result;
    }

    public <T> T getByFastJson(final String key, Class<T> clazz) {
        String str = this.redisTemplate.execute((RedisConnection connection) -> {
            RedisSerializer<String> serializer = RedisUtil.this.redisTemplate.getStringSerializer();
            byte[] value = connection.get(serializer.serialize(key));
            return serializer.deserialize(value);
        });
        if (CommonUtil.isEmpty(str)) {
            return null;
        } else {
            try {
                return JSON.parseObject(str, clazz);
            } catch (Exception var5) {
                log.error("json异常：", var5);
                throw new RuntimeException("Redis 反序列化异常", var5);
            }
        }
    }

    public boolean setNX(final String key, final String value, final long expire) {
        try {
            String result = redisTemplate.execute((RedisCallback<String>) connection -> {
                try {
                    JedisCommands commands = (JedisCommands) connection.getNativeConnection();
                    return commands.set(key, value, "NX", "PX", expire);
                } catch (ClassCastException e) {
                    log.warn("#[ {} ] setNX. ClassCastException.", key);
                    return null;
                }
            });
            return !StringUtils.isEmpty(result);
        } catch (Exception e) {
            log.error("set redis occured an exception", e);
            throw new RedisLockException("set redis occured an exception : " + e.getMessage());
        }
    }

    public boolean release(final String UNLOCK_LUA, final List<String> keys, final List<String> args){
        // 使用lua脚本删除redis中匹配value的key，可以避免由于方法执行时间过长而redis锁自动过期失效的时候误删其他线程的锁
        // spring自带的执行脚本方法中，集群模式直接抛出不支持执行脚本的异常，所以只能拿到原redis的connection来执行脚本
        Long result = redisTemplate.execute((RedisCallback<Long>) connection -> {
            Object nativeConnection = connection.getNativeConnection();
            // 集群模式和单机模式虽然执行脚本的方法一样，但是没有共同的接口，所以只能分开执行
            // 集群模式
            if (nativeConnection instanceof JedisCluster) {
                return (Long) ((JedisCluster) nativeConnection).eval(UNLOCK_LUA, keys, args);
            }

            // 单机模式
            else if (nativeConnection instanceof Jedis) {
                return (Long) ((Jedis) nativeConnection).eval(UNLOCK_LUA, keys, args);
            }
            return 0L;
        });
        return result != null && result > 0;
    }

